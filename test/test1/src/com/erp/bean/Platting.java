package com.erp.bean;



public class Platting {
	private Integer id;

	private String Internal_PN;

	private String Layer_Name;

	private String Plating_Area;

	private String Plating_Type;

	private String Plating_2ND;

	private String Input_User;

	private String Input_User_CN;

	private String Date_Time;
	

	private String Status;


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getInternal_PN() {
		return Internal_PN;
	}


	public void setInternal_PN(String internal_PN) {
		Internal_PN = internal_PN;
	}


	public String getLayer_Name() {
		return Layer_Name;
	}


	public void setLayer_Name(String layer_Name) {
		Layer_Name = layer_Name;
	}


	public String getPlating_Area() {
		return Plating_Area;
	}


	public void setPlating_Area(String plating_Area) {
		Plating_Area = plating_Area;
	}


	public String getPlating_Type() {
		return Plating_Type;
	}


	public void setPlating_Type(String plating_Type) {
		Plating_Type = plating_Type;
	}


	public String getPlating_2ND() {
		return Plating_2ND;
	}


	public void setPlating_2ND(String plating_2nd) {
		Plating_2ND = plating_2nd;
	}


	public String getInput_User() {
		return Input_User;
	}


	public void setInput_User(String input_User) {
		Input_User = input_User;
	}


	public String getInput_User_CN() {
		return Input_User_CN;
	}


	public void setInput_User_CN(String input_User_CN) {
		Input_User_CN = input_User_CN;
	}


	public String getDate_Time() {
		return Date_Time;
	}


	public void setDate_Time(String date_Time) {
		Date_Time = date_Time;
	}


	public String getStatus() {
		return Status;
	}


	public void setStatus(String status) {
		Status = status;
	}


	@Override
	public String toString() {
		return "Platting [id=" + id + ", Internal_PN=" + Internal_PN + ", Layer_Name=" + Layer_Name + ", Plating_Area="
				+ Plating_Area + ", Plating_Type=" + Plating_Type + ", Plating_2ND=" + Plating_2ND + ", Input_User="
				+ Input_User + ", Input_User_CN=" + Input_User_CN + ", Date_Time=" + Date_Time + ", Status=" + Status
				+ "]";
	}



	
	
	
	
	
	


}
